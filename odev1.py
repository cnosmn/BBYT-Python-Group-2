
# 4 tane degisken oluşturucaz bu değişkenler 2 tanesi tam sayı 2 tanesi ondalık sayı olacak
# 4 değişkenin toplamını, çarpımını,çıkarma ve bölme işlem sonucları ekrana yazdıracağız
# değişken isimlendirme kurallarına uyacağız
# sonucları degiskene atama yapıcaz   print(sayi1 + sayi2)  -> bu yanlış 
# toplam = sayi1 + sayi2  -> bu doğru  print(toplam)

sayi1 = 10
sayi2 = -25

ondalik1 = 15.5
ondalik2 = 4.5

toplam = sayi1 + sayi2 + ondalik1 + ondalik2
cikarma = sayi1 - sayi2 - ondalik1 - ondalik2
carpim = sayi1 * sayi2 * ondalik1 * ondalik2
bolme = sayi1 / sayi2 / ondalik1 / ondalik2  

print("Toplam: ", toplam)
print("Çıkarma: ", cikarma)
print("Çarpım: ", carpim)
print("Bölme: ", bolme)


